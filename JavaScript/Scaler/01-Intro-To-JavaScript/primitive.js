// number
let a = 10;
console.log(typeof a);

// string
a = "String";
console.log(a, typeof a);

// boolean
a = true;
console.log(typeof a);

// null 
a = null;
console.log(typeof a);

// undefined
let b;
console.log(typeof b);

// bigint
a = 1237862367786n;
console.log(typeof a);

// symbol
a = Symbol('123');
console.log(a, typeof a);