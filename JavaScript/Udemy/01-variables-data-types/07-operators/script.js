// 1. Arithmetic Operators
let x;

x = 5+5;
console.log(x);
x = 5-5;
console.log(x);
x = 5/5;
console.log(x);
x = 7%5;
console.log(x);

// 2. Assignment Operators
// Concatenation
x = 'Hello'+' '+'World';
console.log(x);

// Exponent
x = 2**3;
console.log(x);

// Increment
x += 1;
x++;
console.log(x);

// Decrement
x -= 1;
x--;
console.log(x);

// 3. Comparison Operators
x = 2 == '2';
console.log(x);

x = 2 === '2';
console.log(x);

x = 2 != '2';
console.log(x);

x = 2 !== '2';
console.log(x);