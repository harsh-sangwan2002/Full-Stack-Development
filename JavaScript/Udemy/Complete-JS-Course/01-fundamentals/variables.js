// 1. number
let x = 2;
console.log(x);
console.log(typeof x);

// 2. string
x = "2";
console.log(x);
console.log(typeof x);

// 3. boolean
x = true;
console.log(x);
console.log(typeof x);

// 4. undefined
let y;
console.log(y);
console.log(typeof y);

// 5. null
x = null;
console.log(x);
console.log(typeof x);

// 6. bigint
x = 1236526735762354n;
console.log(x);
console.log(typeof x);

// 7. symbol
x = Symbol('Harsh');
console.log(x);
console.log(typeof x);